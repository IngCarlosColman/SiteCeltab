    <h3 class="headline">Descripción del Proyecto</h3><span class="line"></span><div class="clearfix"></div>
    <p>The domotic term is from the union of the latin word “domus” (house) with “robotic” and is the study of the realization of an intelligent home environment. It’s functionalities are since watering plants and controlling the ambient illumination to managing the electric grid of a house or establishment.</p>
	<p>This line of research is focused on the study and development of a smart office, remotely controlled through web applications and smartphones.</p>
	<p>Although a large variety of applications are available on the IT market, being either free or proprietary, this project is focused on the concept of Smart Grid, in other words, obtain better energetic control through automation, establishing a two-way connection between the electrical box with the devices and sensors office. All technological solutions are open and at a low cost to the community.</p>
	<p><strong>Researchers:</strong> Kenner Alan Kliemann; Augusto Dantas; Marcos Teixeira </p>
 
