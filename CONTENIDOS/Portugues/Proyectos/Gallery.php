    <h3 class="headline">Descrição do Projeto</h3><span class="line"></span><div class="clearfix"></div>
    <p>Este projecto resulta da necessidade de várias unidades da Itaipu Binacional, para manter o registro histórico de todas as actividades no âmbito do reservatório da represa de Itaipu.

    <p>Este registro consiste de milhões de fotografias tiradas nas ações tomadas pela organização ao longo do tempo, o controle consistente e fluxo das diversas fases dessas ações. Cada ação é sub-dividido novamente em bairros e municípios afetados.
    <p>Para agilizar o ordenamento, gestão, captura e classificação destas fotografias, foi tomado como uma ferramenta para a gestão tecnológica, dentre vários previamente analisadas, a ferramenta open source plataforma web chamado "Project Gallery."
    <p>É uma plataforma de gerenciamento de acrílico, baseado em PHP e usar o ambiente chamado de Kohana Framework web. Isto é usado pelos usuários registrados para carregar imagens, fornecendo informações como título, inscrição, comentários, tags e localização também imagens georreferenciadas.
    <p>Mas, como a ferramenta atende às suas necessidades parcialmente, Itaipu em contato com o CELTAB e solicitou a intervenção do centro de pesquisa para gerar módulos (plugins) que geram as características mais específicas, a ferramenta "Project Gallery".
    <p>Do ponto de aceitação do compromisso de investigação e melhorias na ferramenta, 3 pesquisadores que escreveram 17 módulos personalizados, bem como criaram manuais e tutoriais relativos e criadas e implementadas melhorias.
    <p>No decorrer do tempo, e tendo em vista os bons resultados deste projecto está se espalhando para outras unidades dentro Itaipu, por isso, nós também fornecemos suporte técnico para outras 3 unidades de operação na Usina de Itaipu.
    <p>Com suporte para todos os investigadores, que são parte de CELTAB, acreditamos que cada vez que vamos para muito mais.
    <p><strong>Pesquisadores:</strong>  Mohamad Abu Ali; Fabio Barboza; Carlos Colman </p>
