    <h3 class="headline">Descrição do Projeto</h3><span class="line"></span><div class="clearfix"></div>
    <p>O JoinOS é um sistema operacional licenciado sob GPL, desenvolvido pelo Centro Latino-Americano de Tecnologias Abertas, com base no projeto Debian Wheezy, com o propósito de trabalhar juntos para acabar usuário proporcionando um ambiente amigável, seguro, estável e produtiva.
    <p>O projeto JoinOS surgiu a partir da necessidade de facilitar a migração de cerca de 3200 postos de trabalho a partir de Itaipu, que atualmente utiliza software proprietário, garantindo que os usuários não percam a sua produtividade e minimizar custos de treinamento.
    <p>O JoinOS tem uma versão de 32 bits estável, e um desenvolvimento de 64 bits verão. Através de uma interface intuitiva e agradável, visando a nulidade da curva de aprendizado causada na maioria dos processos de migração, garantindo uma rápida capacidade de adaptação à nova plataforma.
