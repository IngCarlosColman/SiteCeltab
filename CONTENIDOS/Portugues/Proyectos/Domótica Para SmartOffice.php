    <h3 class="headline">Descrição do Projeto</h3><span class="line"></span><div class="clearfix"></div>
    <p>A automação residencial termo vem da união da palavra latina "Domus" (casa) com "robótica" è o estudo e implementação de um ambiente de casa inteligente. As Funcionalidades são varias, como controle ambiental, gestão da rede de iluminação de uma casa ou estabelecimento.</p>
	<p>Esta linha de investigação centra-se no estudo e desenvolvimento de escritório inteligente, controlado remotamente via aplicações web e smartphones.</p>
	<p>Embora uma variedade de aplicações estão disponíveis no mercado de TI, seja livre ou proprietário, este projecto centra-se no conceito de Smart Grid, ou seja, obter um melhor controle de energia por meio da automação, que estabelece um conexão bidirecional entre os dispositivos elétricos e sensores. Todas as soluções tecnológicas estão abertas e a um baixo custo para a comunidade.</p>
	<p><strong>Pesquisadores:</strong> Kenner Alan Kliemann; Augusto Dantas; Marcos Teixeira </p>
 
