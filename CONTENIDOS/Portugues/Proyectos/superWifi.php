    <h3 class="headline">Descrição do Projeto</h3><span class="line"></span><div class="clearfix"></div>
    <p>O Super Wi-Fi é um projeto de desenvolvimento conjunto com a Universidade de Rice, que tem várias tecnologias de software protocolo esforços de desenvolvimento, de hardware e de rede projetada para transportar definido pelo rádio cognitivo e software flexível maturação.</p>
    <p>A visão geral é desenvolver um baixo custo, flexível plataforma de rádio de alta velocidade, que pode alcançar distâncias mais longas do que a atual tecnologia Wi-Fi. Isso é feito usando freqüências UHF rádio livre, com o desenvolvimento de novos hardware e software.</p>
    <p>A principal tarefa do CELTAB dentro do projeto é testar a versão atual do sistema, e desenvolver um driver de rede para Linux para a próxima geração do sistema.</p>
    <p><strong>Pesquisadores:</strong> Arthur Garcete; Gustavo Valiati; Fabio Barboza; Jônios Costa Máximo</p>
