    <h3 class="headline">Descripción del Proyecto</h3><span class="line"></span><div class="clearfix"></div>
    <p>JoinOS es un sistema operativo con licencia GPL, desarrollado por el Centro Latino-Americano de Tecnologias Abiertas, basado en proyecto Debian Wheezy, con el propósito de trabajar en conjunto con el usuario final proporcionando un ambiente agradable, seguro, estable y productiva.</p>
    <p>El proyecto JoinOS surgió de la necesidad de facilitar la migración de aproximadamente 3,200 estaciones de trabajo de Itaipú, que en la actualidad utiliza el software propietario, lo que garantiza que los usuarios no pierdan su productividad y minimizar los costes de formación y entrenamiento.</p> 
    <p>El JoinOS tiene una versión de 32 bits estable, y un desarrollo avanzado en 64-bit. A través de una interfaz intuitiva y agradable, en busca de la nulidad de la curva de aprendizaje causado en la mayoría de los procesos de migración, garantizando una capacidad de adaptación rápida a la nueva plataforma.</p>
    <p><strong>Investigador:</strong> Jônios Costa Máximo</p>
