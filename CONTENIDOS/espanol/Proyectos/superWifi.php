Súper Wi-fi
<div class="twelve columns alpha">
    <h3 class="headline">Descripción del Proyecto</h3><span class="line"></span><div class="clearfix"></div>
    <p>El Super Wi-Fi es un proyecto de desarrollo conjunto con la Universidad de Rice, que consta de varios esfuerzos de desarrollo de protocolo de software, hardware y de red diseñados para llevar las tecnologías de radio definidos por software cognitivas y flexibles para la maduración.</p>
    <p>La visión general es desarrollar un bajo costo, flexible, plataforma de radio de alta velocidad, que puede alcanzar distancias mayores que la actual tecnología Wi-Fi. Esto se hace mediante el uso de frecuencias de radio UHF libres, con desarrollo de nuevo hardware y software.</p>
    <p>La principal tarea del CELTAB dentro del proyecto es la de probar la versión actual del sistema, así como el desarrollo de un controlador de red para Linux para la próxima generación del sistema.</p>
    <p><strong>Investigadores:</strong> Arthur Garcete; Gustavo Valiati; Fabio Barboza; Jônios Costa Máximo</p>
