    <h3 class="headline">Descrição do Projeto</h3><span class="line"></span><div class="clearfix"></div>
    <p>Um Scanner de código de barras que pode ler vários tipos de códigos de barras usando um telefone inteligente com uma câmera. Ele tira uma foto do código de barras e, em seguida, descodificados a partir da imagem.</p>
    <p>É usado na Usina de Itaipu ao lado de outra solução de software para identificar informações sobre algumas máquinas usando apenas uma etiqueta de código de barras e um scanner. O núcleo para o barcode scanner é baseado no projeto zbar js.</p>
    <p>Este projeto foi feito usando a plataforma Apache Cordova, por isso, é possível recompilar-lo e usá-lo em várias plataformas móveis. Ele já foi testado em Android e Windows 8.1.</p>
    <p><strong>Pesquisadores:</strong> Jônios Costa Máximo; Thiago Bitencourt; Mohammad Abu Ali </p>
