Itaipú BarcodeScanner
<div class="twelve columns alpha">
    <h3 class="headline">Descripción del Proyecto</h3><span class="line"></span><div class="clearfix"></div>
    <p>Un escáner de código de barras que puede leer varios tipos de códigos de barras usando un teléfono inteligente con una cámara. Toma una foto del código de barras y luego decodifica de la imagen. Se utiliza en Itaipú DAM lado de otra solución de software para identificar información sobre algunas máquinas que utilizan solamente una etiqueta de código de barras y un escáner.</p>
    <p>El núcleo para el escáner de código de barras se basa en el proyecto zbarjs.</p>
    <p>Este proyecto se realizó utilizando la plataforma Apache Cordova, así que es posible volver a compilar y usarlo en varias plataformas móviles. Ya se puso a prueba en Android y Windows 8.1.</p>
    <p><strong>Investigadores:</strong> jonios Maximo; Thiago Bitencourt; Mohammad Abu Ali </p>
